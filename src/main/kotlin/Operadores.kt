fun main(){

    var a = 7
    var b = 2
    // OPERADORES ARITMÉTICOS

//    + 	Suma
//    - 	Resta
//    * 	Multiplicación
//    / 	División
//    % 	Resto de división entera
//    ++ 	Auto incremento
//    -- 	Auto decremento

    println("($a + $b)= ${a + b}")
    println("($a - $b)= ${a - b}")
    println("($a x $b)= ${a * b}")
    println("($a / $b)= ${a / b}")
    println("($a % $b)= ${a % b}")

    // OPERADORES LÓGICOS

//    && 	AND o "Y" lógica
//    || 	OR u "O" lógica
//    ! 	NOT o negación (cambio de valor)

    // OPERADORES RELACIONALES

//    > 	Mayor que
//    < 	Menor que
//    >= 	Mayor o igual que
//    <= 	Menor o igual que
//    == 	Igual que
//    != 	Distinto de

    // OPERADORES DE ASIGNACIÓN

//    = 	Asignación simple
//    += 	Autosuma
//    -= 	Autoresta
//    *= 	Automultiplicación
//    /= 	Autodivisión
//    %= 	Autoresto

    a= 10
    b= 20
    a += b
    println("+= $a")
    a -= b
    println("-= $a")
    a *= b
    println("*= $a")
    a /= b
    println("/= $a")
    a %= b
    println("%= $a")
}